const NAMESPACES = {
  pmsFrontdesk: FRONTDESK,
  pmsChannels: CHANNELS,
  pmsTarifs: TARIFS,
};

const LOCALES = ["fr", "en", "de", "pt", "lb"];

let totalAdded = 0;
for (const locale of LOCALES) {
  const file = path.join(MSG_DIR, `${locale}.json`);
  const data = JSON.parse(fs.readFileSync(file, "utf8"));
  let added = 0;
  for (const [ns, byLocale] of Object.entries(NAMESPACES)) {
    const keys = byLocale[locale];
    if (!keys) throw new Error(`Missing locale ${locale} for namespace ${ns}`);
    data[ns] = keys;
    added += Object.keys(keys).length;
  }
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + "\n");
  console.log(`Updated ${locale}: +${added} keys`);
  totalAdded = added;
}
console.log(`\nTotal keys added per locale: ${totalAdded}`);
